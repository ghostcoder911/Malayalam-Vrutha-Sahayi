    vruthamName = vruthamParts[0]
    if vruthamParts[1][:-1][0] == '(':
        chandasParts = vruthamParts[1][1:-2].split(',')
        if len(chandasParts) == 2:
            chandas = (
             int(chandasParts[0]), int(chandasParts[1]))
        else:
            chandas = (
             int(chandasParts[0]), int(chandasParts[1]), int(chandasParts[2]), int(chandasParts[3]))
